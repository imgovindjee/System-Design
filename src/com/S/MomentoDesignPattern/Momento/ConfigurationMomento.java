package com.S.MomentoDesignPattern.Momento;

public class ConfigurationMomento {
    int height;
    int width;

//    CONSTRUCTOR
    public ConfigurationMomento(int height, int width) {
        this.height = height;
        this.width = width;
    }

//    GETTER
    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}
